def get_duration(term):
    """
    根据期限获取估算的修正久期

    Parameters:
    -----------
    term : str
        期限分类（短期、中短期、中长期、长期、超长期）

    Returns:
    --------
    float
        修正久期（年）
    """
    return DURATION_MAP.get(term, 4.0)


def get_bond_classify2(bond_type):
    """
    获取债券类型对应的数据库classify2字段

    Parameters:
    -----------
    bond_type : str
        债券类型

    Returns:
    --------
    str
        classify2字段值
    """
    return CLASSIFY2_MAPPING.get(bond_type, '')


