export function getCategoryName(categoryId: string, locale: string): string {
  const cat = ASSISTANT_CATEGORIES.find((c) => c.id === categoryId);
  return cat?.nameI18n[locale] || cat?.nameI18n['en-US'] || categoryId;
}

export type AssistantPreset = {
  id: string;
  avatar: string;
  presetAgentType?: PresetAgentType;
  /** Category for grouping this assistant in the UI */
  category?: AssistantPresetCategory;
  /**
   * Directory containing all resources for this preset (relative to project root).
   * If set, both ruleFiles and skillFiles will be resolved from this directory.
   * Default: rules/ for rules, skills/ for skills
   */
  resourceDir?: string;
  /**
   * Path to a prompt-library template JSON (relative to project root).
   * When set, prompt_template field in the JSON is used as the rule/context.
   */
  templatePath?: string;
  ruleFiles: Record<string, string>;
  skillFiles?: Record<string, string>;
  /**
   * Default enabled skills for this assistant (skill names from skills/ directory).
   * 此助手默认启用的技能列表（来自 skills/ 目录的技能名称）
   */
  defaultEnabledSkills?: string[];
  nameI18n: Record<string, string>;
  descriptionI18n: Record<string, string>;
  promptsI18n?: Record<string, string[]>;
};

